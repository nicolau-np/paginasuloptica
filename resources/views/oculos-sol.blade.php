                                                    <p>Com protecção UV, proporcionam uma visão confortável
                                                        e descontraída em ambiente exterior e interior,
                                                        escurecendo ou aclareando de acordo com a
                                                        intensidade luminosa. O ideal para quem pretende uma
                                                        solução 2 em 1.</p>
                                                    <p><strong>COM LENTES POLARIZADAS</strong></p>
