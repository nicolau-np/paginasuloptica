                                                    <p>A pele, os olhos e a área à sua volta são muito
                                                        sensíveis à radiação UV. A maior parte de nós, sabe
                                                        que a radiação UV danifica a pele, provocando
                                                        envelhecimento precoce, rugas e, em casos extremos,
                                                        cancro da pele. Mas, a exposição prolongada à
                                                        radiação UV, também pode provocar várias doenças
                                                        oculares, como cataratas, degeneração macular e
                                                        envelhecimento acelerado da pele em redor dos olhos.
                                                    </p>
